const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    // write your solution here
	
	let res = '';
	for (let i  = 0; i <= expr.length - 10; i += 10 ) {
		let letterMorse = expr.slice(i, i + 10)
		.replace(/^0+/g,'')
	        .replace(/11/g, '-')
	        .replace(/10/g, '.');
	
		res+= MORSE_TABLE[letterMorse] !== undefined 
			? MORSE_TABLE[letterMorse]
			: ' ';
	}
	return res;

}

module.exports = {
    decode
}
